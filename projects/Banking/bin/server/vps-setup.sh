#!/bin/bash
# vps-setup.sh - Automated Banking App VPS Setup v2.0
# Usage: curl -sSL https://raw.githubusercontent.com/rhixecompany/banking/main/scripts/server/vps-setup.sh | bash
# Or: ./scripts/server/vps-setup.sh

set -e

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
CYAN='\033[0;36m'
NC='\033[0m'

# Configuration
REPO_URL="https://github.com/rhixecompany/banking.git"
INSTALL_DIR="/opt/banking"
PROFILE="default"

# Parse arguments
while [[ $# -gt 0 ]]; do
    case $1 in
        --dir)
            INSTALL_DIR="$2"
            shift 2
            ;;
        --profile)
            PROFILE="$2"
            shift 2
            ;;
        --help)
            echo "Usage: $0 [OPTIONS]"
            echo ""
            echo "Options:"
            echo "  --dir PATH     Installation directory (default: /opt/banking)"
            echo "  --profile PROFILE  Docker compose profile (default, local, monitoring)"
            echo "  --help        Show this help message"
            exit 0
            ;;
        *)
            echo "Unknown option: $1"
            exit 1
            ;;
    esac
done

echo -e "${BLUE}========================================${NC}"
echo -e "${BLUE}  Banking App VPS Setup v2.0${NC}"
echo -e "${BLUE}========================================${NC}"
echo ""

# Detect if running as root and get IP
if [ "$EUID" -ne 0 ]; then
    echo -e "${YELLOW}Note: Running without root. Some features may require sudo.${NC}"
    echo ""
fi

SERVER_IP=$(hostname -I | awk '{print $1}')
if [ -z "$SERVER_IP" ]; then
    SERVER_IP="localhost"
fi
echo -e "${YELLOW}Detected IP: ${SERVER_IP}${NC}"
echo ""

# Check for existing installation
if [ -d "$INSTALL_DIR" ]; then
    echo -e "${YELLOW}Warning: ${INSTALL_DIR} already exists.${NC}"
    read -p "Update existing installation? (y/N): " -n 1 -r
    echo ""
    if [[ $REPLY =~ ^[Yy]$ ]]; then
        echo -e "${GREEN}Updating existing installation...${NC}"
        cd "$INSTALL_DIR"
        git pull origin main
    else
        echo -e "${YELLOW}Skipping update.${NC}"
        exit 0
    fi
else
    echo -e "${GREEN}Step 1: Installing Docker...${NC}"
    if ! command -v docker &> /dev/null; then
        echo "Installing Docker..."
        curl -fsSL https://get.docker.com | sh
        systemctl enable docker 2>/dev/null || true
        systemctl start docker 2>/dev/null || true
        echo -e "${GREEN}Docker installed successfully${NC}"
    else
        echo -e "${GREEN}Docker already installed${NC}"
    fi
    echo ""

    echo -e "${GREEN}Step 2: Cloning repository...${NC}"
    mkdir -p "$(dirname "$INSTALL_DIR")"
    git clone "$REPO_URL" "$INSTALL_DIR"
    cd "$INSTALL_DIR"
    echo -e "${GREEN}Repository cloned to ${INSTALL_DIR}${NC}"
    echo ""
fi

cd "$INSTALL_DIR"

# Setup directories
echo -e "${GREEN}Step 3: Setting up directories...${NC}"
mkdir -p "$INSTALL_DIR/compose/traefik/certs"
mkdir -p "$INSTALL_DIR/compose/traefik/auth"
mkdir -p "$INSTALL_DIR/.envs/production"
echo -e "${GREEN}Directories created${NC}"
echo ""

# Generate environment file
echo -e "${YELLOW}========================================${NC}"
echo -e "${YELLOW}  Environment Configuration${NC}"
echo -e "${YELLOW}========================================${NC}"
echo ""

# Generate secrets
ENCRYPTION_KEY=$(openssl rand -hex 32)
NEXTAUTH_SECRET=$(openssl rand -base64 32)

echo -e "${GREEN}Generated secrets:${NC}"
echo "  ENCRYPTION_KEY: ${ENCRYPTION_KEY:0:16}..."
echo "  NEXTAUTH_SECRET: ${NEXTAUTH_SECRET:0:16}..."
echo ""

# Create environment file
echo -e "${GREEN}Step 4: Creating environment file...${NC}"
cat > "$INSTALL_DIR/.envs/production/.env.production" << EOF
# Banking Application - Production Environment
# Generated by vps-setup.sh

# Application
NODE_ENV=production
PORT=3000
HOSTNAME=0.0.0.0
DOMAIN=${SERVER_IP}
NEXT_PUBLIC_SITE_URL=http://${SERVER_IP}

# Security
ENCRYPTION_KEY=${ENCRYPTION_KEY}
NEXTAUTH_SECRET=${NEXTAUTH_SECRET}

# Database
DATABASE_URL=postgresql://postgres:banking_secure_pass@postgres:5432/banking
POSTGRES_USER=postgres
POSTGRES_PASSWORD=banking_secure_pass
POSTGRES_DB=banking

# Redis
REDIS_URL=redis://redis:6379
REDIS_PASSWORD=banking_redis_pass

# Plaid (optional)
PLAID_CLIENT_ID=
PLAID_SECRET=
PLAID_ENV=sandbox

# Dwolla (optional)
DWOLLA_KEY=
DWOLLA_SECRET=
DWOLLA_BASE_URL=https://api-sandbox.dwolla.com
DWOLLA_ENV=sandbox

# Docker
COMPOSE_PROFILE=${PROFILE}
EOF

echo -e "${GREEN}Environment file created at .envs/production/.env.production${NC}"
echo ""

# Generate htpasswd for Traefik
echo -e "${GREEN}Step 5: Generating Traefik credentials...${NC}"
if command -v htpasswd &> /dev/null; then
    echo "admin:admin" | htpasswd -bc "$INSTALL_DIR/compose/traefik/auth/htpasswd" admin banking123
    echo -e "${GREEN}htpasswd created (admin:banking123)${NC}"
else
    echo -e "${YELLOW}htpasswd not found. Using docker to generate...${NC}"
    docker run --rm --entrypoint htpasswd httpd:2 -bc "$INSTALL_DIR/compose/traefik/auth/htpasswd" admin banking123 2>/dev/null || \
    echo -e "${YELLOW}Warning: Could not generate htpasswd. Create manually.${NC}"
fi
echo ""

# Build Docker image
echo -e "${YELLOW}========================================${NC}"
echo -e "${YELLOW}  Build Option${NC}"
echo -e "${YELLOW}========================================${NC}"
echo ""
echo "Choose build option:"
echo "  1) Build locally (10-15 minutes)"
echo "  2) Pull from registry (faster)"
echo ""
read -p "Select option [1-2] (default: 1): " build_choice
build_choice=${build_choice:-1}

cd "$INSTALL_DIR"

if [ "$build_choice" -eq 2 ]; then
    echo -e "${GREEN}Pulling Docker image...${NC}"
    docker pull ghcr.io/rhixecompany/banking:latest || \
    echo -e "${YELLOW}Warning: Could not pull image. Building locally instead.${NC}"
    build_choice=1
fi

if [ "$build_choice" -eq 1 ]; then
    echo -e "${GREEN}Building Docker image locally...${NC}"
    echo -e "${YELLOW}This may take 10-15 minutes...${NC}"
    docker compose build --build-arg NEXT_PUBLIC_SITE_URL=http://${SERVER_IP}
fi
echo ""

# Run migrations
echo -e "${GREEN}Step 6: Running database migrations...${NC}"
docker compose --profile init up
echo -e "${GREEN}Migrations complete${NC}"
docker compose --profile init down
echo ""

# Start services
echo -e "${GREEN}Step 7: Starting services...${NC}"
if [ "$PROFILE" = "monitoring" ]; then
    docker compose --profile monitoring up -d
elif [ "$PROFILE" = "local" ]; then
    docker compose --profile local up -d
else
    docker compose up -d
fi
echo ""

# Wait for services
echo -e "${GREEN}Step 8: Waiting for services to be healthy...${NC}"
sleep 10

# Check health
for i in {1..30}; do
    if curl -sf http://localhost:3000/api/health > /dev/null 2>&1; then
        echo -e "${GREEN}Application is healthy!${NC}"
        break
    fi
    echo -n "."
    sleep 2
done
echo ""

# Final status
echo ""
echo -e "${BLUE}========================================${NC}"
echo -e "${BLUE}  Deployment Complete!${NC}"
echo -e "${BLUE}========================================${NC}"
echo ""

echo -e "${GREEN}Service Status:${NC}"
docker compose ps
echo ""

echo -e "${YELLOW}========================================${NC}"
echo -e "${YELLOW}  Access Information${NC}"
echo -e "${YELLOW}========================================${NC}"
echo ""
echo -e "Banking App:       http://${SERVER_IP}:3000"
echo -e "Traefik Dashboard: http://${SERVER_IP}:8090"
echo -e "  Username: admin"
echo -e "  Password: banking123"
echo ""
echo -e "${YELLOW}Note: HTTPS uses self-signed certificate${NC}"
echo ""

echo -e "${GREEN}========================================${NC}"
echo -e "${GREEN}  Useful Commands${NC}"
echo -e "${GREEN}========================================${NC}"
echo ""
echo "View logs:"
echo "  docker compose logs -f"
echo ""
echo "Stop services:"
echo "  docker compose down"
echo ""
echo "Update:"
echo "  cd $INSTALL_DIR && git pull origin main"
echo "  docker compose build"
echo "  docker compose up -d"
echo ""
echo "Cleanup:"
echo "  docker compose down -v"
echo ""

echo -e "${CYAN}========================================${NC}"
echo -e "${CYAN}  Next Steps${NC}"
echo -e "${CYAN}========================================${NC}"
echo ""
echo "1. Update your DNS to point to ${SERVER_IP}"
echo "2. Edit .envs/production/.env.production with real API keys"
echo "3. Set up HTTPS with Let's Encrypt (see docs/docker/production.md)"
echo ""
